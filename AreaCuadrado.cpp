


#include <iostream>

using namespace std;

int main() {
    // Declarar variables
    double lado, area;

    // Solicitar al usuario la longitud de un lado
    cout << "Ingrese la longitud de un lado del cuadrado: ";
    cin >> lado;

    // Calcular el área del cuadrado
    area = lado * lado;

    // Mostrar el resultado
    cout << "El área del cuadrado es: " << area << endl;

    return 0;
}













